// Comprehensive utility functions for searching, filtering, and data manipulation
const useCaseUtils = {
  // Search functionality
  search: (keyword) => {
    if (!keyword || keyword.trim() === '') return useCases;
    const searchTerm = keyword.toLowerCase().trim();
    return useCases.filter(uc => 
      uc.title.toLowerCase().includes(searchTerm) ||
      uc.description.toLowerCase().includes(searchTerm) ||
      uc.industry.toLowerCase().includes(searchTerm) ||
      uc.tags.some(tag => tag.toLowerCase().includes(searchTerm))
    );
  },

  // Filter by industry
  getByIndustry: (industry) => {
    if (!industry) return useCases;
    return useCases.filter(uc => uc.industry.toLowerCase().includes(industry.toLowerCase()));
  },

  // Filter by tag
  getByTag: (tag) => {
    if (!tag) return useCases;
    return useCases.filter(uc => uc.tags.includes(tag.toLowerCase()));
  },

  // Filter by difficulty
  getByDifficulty: (difficulty) => {
    if (!difficulty) return useCases;
    return useCases.filter(uc => uc.difficulty.toLowerCase() === difficulty.toLowerCase());
  },

  // Get random selection
  getRandom: (count = 5) => {
    const shuffled = [...useCases].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, Math.min(count, useCases.length));
  },

  // Get featured use cases (first 10)
  getFeatured: () => useCases.slice(0, Math.min(10, useCases.length)),

  // Get use case by ID
  getById: (id) => useCases.find(uc => uc.id === parseInt(id)),

  // Get statistics
  getStats: () => ({
    total: useCases.length,
    byIndustry: industries.reduce((acc, industry) => {
      acc[industry] = useCases.filter(uc => uc.industry === industry).length;
      return acc;
    }, {}),
    byTag: useCases.reduce((acc, uc) => {
      uc.tags.forEach(tag => {
        acc[tag] = (acc[tag] || 0) + 1;
      });
      return acc;
    }, {}),
    byDifficulty: {
      'Beginner': useCases.filter(uc => uc.difficulty === 'Beginner').length,
      'Intermediate': useCases.filter(uc => uc.difficulty === 'Intermediate').length,
      'Advanced': useCases.filter(uc => uc.difficulty === 'Advanced').length
    }
  }),

  // Pagination helper
  paginate: (page = 1, perPage = 10) => {
    const startIndex = (page - 1) * perPage;
    const endIndex = startIndex + perPage;
    return {
      data: useCases.slice(startIndex, endIndex),
      pagination: {
        currentPage: page,
        perPage: perPage,
        totalItems: useCases.length,
        totalPages: Math.ceil(useCases.length / perPage),
        hasNext: endIndex < useCases.length,
        hasPrev: page > 1
      }
    };
  }
};

